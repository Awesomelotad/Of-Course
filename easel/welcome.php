            While the knowledge, skills and understandings that are gained in study are important; it is equally - if not more - important that we learn how to learn.<br/>
            Futurists believe that youth who are leaving school now will have around eight career changes in their lives. Not eight different jobs but complete career changes. It is important when making subject choices that learners think not just about what they need for their first career choice, but that they will need to go on learning and retrain a number of times in the future.<br/><br/>
            The vast majority of our learners will continue with academic study and/or training when they leave Whangaparaoa College. Success at school allows people to have a wider range of choices about what they do in the future. When considering their choices learners should take every opportunity to talk to people and get advice.<br/>
