<div class="Types Container">
    <h1 class="Important">- Course Types and Difficulty -</h1>
    <div class="Content">
        <ul>
            <li>
                <b>01 courses</b> are the full courses. They are based on national curriculum documents with mainly achievement standard assessments. 
                Total credit value: usually between 16 and 24 credits.<br/>
                Run for the whole year with a learning programme of 4 periods each week.<br/><br/>
            </li>
            <li>
                <b>02 and 03 courses</b> are alternative full courses catering for particular learner and / or local needs. These courses are likely to have unit standards as well as achievement standards used for assessment, and / or have a reduced number of total credits offered. 
                Total credit value: between 12 and 24 credits.<br/>
                Run for the whole year with a learning programme of 4 periods each week.<br/><br/>
            </li>
            <li>
                <b>05 indicates a short course for level 1 only</b>. They may have a mixture of achievement and unit standard assessments.
                Total credit value: between 8 and 12 credits.<br/>
                Run for the whole year with a learning programme of approximately 2 hours each week.<br/><br/>
            </li>
            <li>
                100 level courses are Level 1 courses; they are first taken in Year 11. 200 level courses are Level 2 courses. 300 level courses are Level 3 courses.   
                <b>Learners can study a multi-level course programme, consisting of a mixture of courses at Levels 1, 2, and 3, as long as pre-requisites and co-requisites have been met.</b><br/><br/>
            </li>
            <li>
                <b>Gateway</b> and <b>sports programmes</b> at Years 11, 12, and 13 will be developed in conjunction with individual learning programmes.
            </li>
        </ul>
    </div>
</div>