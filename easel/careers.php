        <p>
            Information about careers can be found on the 'Kiwicareers' website: <a href="http://www.kiwicareers.govt.nz">www.kiwicareers.govt.nz</a>. This site is worth browsing - it is constantly updated. 
            The best way to begin planning for the future is with <b>the who am I?</b> question rather than the <b>what's out there?</b>  question. Developing a clear understanding of self-i.e. skills, abilities, interests, values, work/learning styles, strengths and weaknesses enables students to narrow the list of options to those that best suit them. Beginning with the "What's out there?" question without having a clear sense of self-awareness can lead to confusion and a feeling of being overwhelmed by information.<br/>
